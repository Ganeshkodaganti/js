//A java script array can be created in 3 wayss

//By array literal
var arr = [10, 20, 30, 40, 50, 60, 70];


//By creating instance of array directly
var arr1 = new Array();
arr1[0] = 10;
arr1[1] = 20;
arr1[2] = 30;

//By using an array constructor
var arr3 = new Array(10, 20, 30, 40, 50, 60, 70, 80);


//printing the three arrays

console.log(arr);
console.log(arr1);
console.log(arr3);

//length property will return the length of the array

console.log(arr.length);